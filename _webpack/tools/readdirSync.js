const fs = require("fs");
const path = require("path");

const readdirSync = (dir) => {
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => path.join(dir, d.name));
};

module.exports = readdirSync;
